package java04;

public class IntValueExample {
    public static void main(String[] args) {
        int decimalValue = 7; // 10진수
        int binVlaue = 0b1000_0101; // 2진수
        int octalValue = 077; // 8진수
        int hexaValue = 0xFEFE; // 16진수

        System.out.println(decimalValue);
        System.out.println(binVlaue);
        System.out.println(octalValue);
        System.out.println(hexaValue);
    }
}
